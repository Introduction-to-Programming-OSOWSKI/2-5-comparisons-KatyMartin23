def greaterThan(x, y):
    if x > y:
        return True
    else:
        return False 
print(greaterThan(7, 5))

def lessThan(x, y):
    if x < y:
        return True
    else:
        return False
print(lessThan(3, 5))

def equalTo(x, y):
    if x == y:
        return True
    else:
        return False
print(equalTo(4, 4))

def greaterOrEqual(x, y):
    if x >= y:
        return True
    else:
        return False
print(greaterOrEqual(6, 4))

def lessOrEqual(x, y):
    if x <= y:
        return True
    else:
        return False
print(lessOrEqual(5, 8))

